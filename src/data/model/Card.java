package data.model;

public class Card {

    private int code;
    private char unicode;
    private int value;



    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public char getUnicode() {
        return unicode;
    }

    public void setUnicode(char unicode) {
        this.unicode = unicode;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }
}
